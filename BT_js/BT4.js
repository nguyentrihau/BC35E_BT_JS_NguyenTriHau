/**
 * Tinh dien tich, Chu vi hinh chu nhat
 * - Dau vao:
 * + Chieu_dai = 5
 * + Chieu_rong = 8
 * 
 * - Xu ly:
 * + Dien_tich = Chieu_dai * Chieu_rong
 * + Chu_Vi = (Chieu_dai + Chieu_rong) * 2
 * 
 * - Dau ra: 
 * Dien_tich = ?
 * Chu_vi = ?
 */

// - Dau vao 
var Chieu_dai = 5;
var Chieu_rong = 8;

// - Xu ly 
var Dien_tich = Chieu_dai * Chieu_rong ;
var Chu_Vi = (Chieu_dai + Chieu_rong) * 2 ;

// - Dau ra 
console.log("Dien tich = "+Dien_tich);
console.log("Chu vi = "+Chu_Vi);